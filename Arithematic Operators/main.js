// ARITHEMATIC OPERATORS:
var x = 16;
var b = 2;
// ADDITION OPERATOR:
console.log(x + b);
// SUBRACTION OPERATOR:
console.log(x - b);
// MULTIPLICATION OPERATOR:
console.log(x * b);
// DIVISION OPERATOR:
console.log(x / b);
// MODULUS/REMAINDER OPERATOR:
console.log(x % b);
// INCREMENT OPERATOR: 
//  PRE-INCREMENT OPERATOR: 
++x;
console.log(x);
//  POST-INCREMENT OPERATOR: 
x++;
console.log(x);
// DECREMENT  OPERATOR:  
b--;
console.log(b);
