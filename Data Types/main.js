// STRING datatype:
var userName = "Maryam Ejaz";
console.log(userName);
// BOOLEAN datatype: 
var type = true;
console.log(type);
// NULL datatype:
var set = null;
console.log(set);
// NUMBER datatype: 
var x = 12;
console.log(x);
// UNDEFINED datatype:
var b = undefined;
console.log(b);
// SYMBOL datatype:
var s1 = Symbol('data');
console.log(s1.description);
// BIGINT datatype:
var value = 2n;
var dataNum = 5n;
var result = value * dataNum;
console.log(result);
